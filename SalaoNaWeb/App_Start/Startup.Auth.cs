            //app.UseTwitterAuthentication(
            //    consumerKey: "",
            //    consumerSecret: "");

            app.UseFacebookAuthentication(
                appId: "865396020197033",
                appSecret: "3191c1e607a64dbd06a723d7f53ca23b");

            //app.UseGoogleAuthentication();
                
        }
    }
}
